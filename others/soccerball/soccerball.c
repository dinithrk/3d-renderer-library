void bubbleSort(float arr[][3], int base) {
    int i, j;
    float temp[3];

    for (i = 0; i < 60; i++) {
        for (j = 0; j < 60-i-1; j++) {

            if (arr[j][base] > arr[j+1][base]) {

                for (int k = 0; k < 3; k++) {
                    
                    temp[k] = arr[j][k];
                    arr[j][k] = arr[j + 1][k];
                    arr[j + 1][k] = temp[k];
                }
            }
        }
    }
}


